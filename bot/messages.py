def get_form(words: list[str], num: int) -> int:
    last_digit = num % 10
    previous_digit = num // 10 % 10

    if previous_digit == 1:
        return words[0]
    if last_digit == 1:
        return words[1]
    if last_digit in [2, 3, 4]:
        return words[2]
    return words[0]


